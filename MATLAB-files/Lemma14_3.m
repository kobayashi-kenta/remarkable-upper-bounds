function Lemma14_3
    pair = [];

    a = sym('a'); b = sym('b');
    d1 = 1 - a; d2 = 1 - 2*a;
    omega = a*(1 - a)/(1 - a + a^2 + b^2);

    f = omega;
    g = 4*a*(1 - a)*(7 - 4*b^2)/21 ...
        - a*d1/21/(1 - a*d1 + b^2) ...
            *(3*d2^2 + 4*(1 - b^2)*(d2^2 + 4*b^2));
    pair = [pair [f; g]];

    f = diff(omega, a);
    g = 4*(1 - 2*a)*(2 - b^2 + 5*a*(1-a))/7 ...
        - d2/7/(1 - a*d1 + b^2)^2*( ...
            d2^2 + a*d1*(8*d2^2*(1 + 4*b^2) + 28*b^4) ...
            + 4*a^2*d1^2*(5*a*d1 + 21*b^2) + b^2*(5 - 4*b^4) ...
        );
    h = (1 - 2*a)*(1 + b^2)/(1 - a*d1 + b^2)^2;
    pair = [pair [f; g] [f; h]];

    f = diff(omega, a, 2);
    g = -2*(11*a*(1-a) + 2*b^2)/7/b^2 ...
        + 2*(1 + b^2)/7/b^2/(1 - a*d1 + b^2)^3*( ...
            a^3*d1^3*(a*d1 + 3*d2^2 + b^2)/(1 + b^2) ...
            + a^2*d1^2*(10*a*d1 + 19*d2^2 + b^2) ...
            + a*d1*d2^2*(1 + 6*d2^2 + 7*b^2) ...
            + a*d1*(2 - 3*b^2)^2 + d2^2*b^4 + 2*b^2*(1 - b^2)^2 ...
        );
    pair = [pair [f; g]];

    f = diff(omega, b);
    g = - 2*a*(1-a)*b/(1 - a*d1 + b^2)^2;
    h = - 1/sym(6)/b*(1 - ((1 - 7*a*d1 + b^2)^2 + 12*a*d1*d2^2) ...
                /(1 - a*d1 + b^2)^2 ...
        );
    pair = [pair [f; g] [g; h]];

    f = diff(omega, b, 2);
    g = - (24 - 64*a*(1 - a) - 17*b^2)/44/b^2 ...
        + 1/sym(44)/b^2/(1 - a*d1 + b^2)^3*( ...
            13*d2^2*b^2 + a^3*d1^3*(8 + 17*b^2) ...
            + d2^4*(1 + 6*b^2 + (3 - 2*a*d1+3*b^2)^2) ...
            + a^2*d1^2*(4 + 10*b^2 + 3*b^2*(1 - b^2)) ...
            + 2*(6*a*d1 + 7*d2^2)*(1 - b^2)*(1 - 3*b^2)^2 ...
            + b^2*(1 - b^2)*(34*d2^2 + 3*a*d1*(13 + 27*b^2) ...
                + 82*d2^2*(1 - b^2) + 17*b^4) ...
        );
    pair = [pair [f; g]];

    for p = pair
        if simplifyFraction(p(1) - p(2)) ~= 0
            error('There is something wrong.');
        end
    end
    disp('It is all right.');
end
