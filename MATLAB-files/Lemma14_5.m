function Lemma14_5
    pair = [];

    a = sym('a'); at = sym('at'); b = sym('b'); bt = sym('bt');
    d1 = 1 - a; d2 = 1 - 2*a;
    phi = b^2/(a^2 + b^2);
    psi = b^2*(1 + 2*a)/(1 + 4*b^2)*phi;
    psi_a = diff(psi, a);
    psi_b = diff(psi, b);
    psi_aa = diff(psi_a, a);
    psi_bb = diff(psi_b, b);

    f = L2ab(a, b);
    g = (1 + a^2 + (1 - a)^2 + 2*b^2)/54 ...
        - b^4/32/(a^2 + b^2)/((1 - a)^2 + b^2);
    h = (1 - a + a^2 + b^2)/27 ...
        - b^4*(1 + 2*a)/32/(a^2 + b^2)/(1 + 4*b^2) ...
        - b^4*(3 - 2*a)/32/((1 - a)^2 + b^2)/(1 + 4*b^2);
    j = (1 - a + a^2 + b^2)/27 - (psi + subs(psi, a, 1 - a))/32;
    pair = [pair [f; g] [f; h] [f; j]];

    f = (1 - a + a^2 + b^2)/27 - 1/sym(32)*( ...
            b^2*(1 + 2*a)/(1 + 4*b^2) ...
            + b^2*(1 + 2*(1 - a))/(1 + 4*b^2) ...
        );
    g = (1 - a + a^2 + b^2)/27 - b^2/8/(1 + 4*b^2);
    pair = [pair [f; g]];

    f = diff(L2ab(a, b), a);
    g = - (1 - 2*a)/27 - (psi_a - subs(psi_a, a, 1 - a))/32;
    pair = [pair [f; g]];

    f = - (1 - 2*a)/27 - 1/sym(32)*( ...
            2*b^2/(1 + 4*b^2) ...
            + 2*b*(1 + 2*(1 - a))/3/(1 + 4*b^2) ...
        );
    g = - (1 - 2*a)/27 - b*(3 - 2*a + 3*b)/48/(1 + 4*b^2);
    pair = [pair [f; g]];

    f = - (1 - 2*a)/27 - 1/sym(32)*( ...
            -2*b*(1 + 2*a)/3/(1 + 4*b^2) - 2*b^2/(1 + 4*b^2) ...
        );
    g = - (1 - 2*a)/27 + b*(1 + 2*a + 3*b)/48/(1 + 4*b^2);
    pair = [pair [f; g]];

    f = subs(diff(L2ab(a, b), a, 2), a, at);
    g = 2/sym(27) ...
        - (subs(psi_aa, a, 1 - at) + subs(psi_aa, a, 1 - at))/32;
    pair = [pair [f; g]];

    f = 2/sym(27) - 1/sym(32)*( ...
            - 2*(3 + 6*at + 4*b)/3/(1 + 4*b^2) ...
            - 2*(3 + 6*(1 - at) + 4*b)/3/(1 + 4*b^2) ...
        );
    g = 2/sym(27) + (3 + 2*b)/12/(1 + 4*b^2);
    pair = [pair [f; g]];

    f = diff(L2ab(a, b), b);
    g = 2*b/27 - (psi_b + subs(psi_b, a, 1 - a))/32;
    pair = [pair [f; g]];

    f = 2*b/27 - 1/sym(32)*( ...
            (1 + 2*a)*(2 + 16*b - 9*b^2)/10/(1 + 4*b^2) ...
            + (1 + 2*(1 - a))*(2 + 16*b - 9*b^2) ...
                /10/(1 + 4*b^2) ...
        );
    g = 2*b/27 - (2 + 16*b - 9*b^2)/80/(1 + 4*b^2);
    pair = [pair [f; g]];

    f = subs(diff(L2ab(a, b), b, 2), b, bt);
    g = 2/sym(27) ...
        - subs(psi_bb + subs(psi_bb, a, 1 - a), b, bt)/32;
    pair = [pair [f; g]];

    f = 2/sym(27) - 1/sym(32)*( ...
            - 21*(1 + 2*a)/11/(1 + 4*bt^2) ...
            - 21*(1 + 2*(1 - a))/11/(1 + 4*bt^2) ...
        );
    g = 2/sym(27) + 21/sym(88)/(1 + 4*bt^2);
    pair = [pair [f; g]];

    f = 432*(1 + 4*b^2)*( ...
            2*((1 - a + a^2 + b^2)/27 - b^2/8/(1 + 4*b^2)) ...
            + b*( ...
                - (1 - 2*a)/27 ...
                - b*(3 - 2*a + 3*b)/48/(1 + 4*b^2) ...
            ) ...
        );
    g = 8 + 8*d2^2 + 16*(1 - b) ...
        + b^2*(18*a + 128*a^2 + 9*b + 28*b^2) ...
        + b*(32*a + 25*b)*(1 - 2*b)^2;
    pair = [pair [f; g]];

    f = 432*(1 + 4*b^2)*( ...
            2*((1 - a + a^2 + b^2)/27 - b^2/8/(1 + 4*b^2)) ...
            - b*( ...
                - (1 - 2*a)/27 ...
                + b*(1 + 2*a + 3*b)/48/(1 + 4*b^2) ...
            ) ...
        );
    g = 8 + 8*d2^2 + 16*(1 - b) ...
        + b^2*(18*d1 + 128*d1^2 + 9*b + 28*b^2) ...
        + b*(32*d1 + 25*b)*(1 - 2*b)^2;
    pair = [pair [f; g]];

    f = 216*(1 + 4*b^2)*( ...
            5*((1 - a + a^2 + b^2)/27 - b^2/8/(1 + 4*b^2)) ...
            - b^2*(2/sym(27) + (3 + 2*b)/12/(1 + 4*b^2)) ...
        );
    g = 9*b*(1 - b) + 15*(2 + b)*(1 - 2*b)^2 ...
        + 10*d2^2*(1 + 4*b^2) ...
        + 96*b*(1 + b)*(1 - b)^2;
    pair = [pair [f; g]];

    f = 2160*(1 + 4*b^2)*( ...
            2*((1 - a + a^2 + b^2)/27 - b^2/8/(1 + 4*b^2)) ...
            + b*(2*b/27 - (2 + 16*b - 9*b^2)/80/(1 + 4*b^2)) ...
        );
    g = 337*b^3 + 40*d2^2*(1 + 4*b^2) + 30*(2 - b - 7*b^2)^2 ...
        + 2*b*(1 - b)*(33 + 352*b + 95*b^2);
    pair = [pair [f; g]];

    f = 108*(1 + 4*b^2)*( ...
            2*((1 - a + a^2 + b^2)/27 - b^2/8/(1 + 4*b^2)) ...
            - b*2*b/27 ...
        );
    g = 3 + 3*(1 - b^2) + 2*d2^2*(1 + 4*b^2);
    pair = [pair [f; g]];

    f = 108*(1 + 4*b^2)*( ...
            4*((1 - a + a^2 + b^2)/27 - b^2/8/(1 + 4*b^2)) ...
            - b^2*(2/sym(27) + 1/sym(4)/(1 + 4*b^2)) ...
        );
    g = 4 + 7*b^2 + 4*d2^2*(1 + 4*b^2) + 8*(1 - 2*b^2)^2;
    pair = [pair [f; g]];

    for p = pair
        if simplifyFraction(p(1) - p(2)) ~= 0
            error('There is something wrong.');
        end
    end
    disp('It is all right.');
end
