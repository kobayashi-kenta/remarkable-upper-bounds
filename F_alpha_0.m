function z = F_alpha_0(s, c1, c2, c3, w1, w2, w3, u0)
    v0 =  (3*c2 + 3*c3 - c1) * w1 ...
        + (3*c3 + 3*c1 - c2) * w2 ...
        + (3*c1 + 3*c2 - c3) * w3;
    z = s / 15 * ( ...
        8 * (c1^2 + c2^2 + c3^2) * (w1 + w2 + w3 - 3*u0)^2 ...
                / (c1 + c2 + c3)^2 ...
        - 2 * (w1 + w2 + w3 - 3*u0) * v0 / (c1 + c2 + c3) ...
        + 5 * (w1^2 + w2^2 + w3^2) ...
    );
end
